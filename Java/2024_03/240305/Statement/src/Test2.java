public class Test2 {
    public static void main(String[] args) {
        // 지시사항 1번을 참고하여 코드를 작성하세요.
        boolean b1 = true || false;

        // 지시사항 2번을 참고하여 코드를 작성하세요.
        boolean b2 = true && false;

        // 지시사항 3번을 참고하여 코드를 작성하세요.
        boolean b3 = (4 < 5) || (7 < 6);

        System.out.println(b1 + ", " + b2 + ", " + b3);
    }
}
